#include "libft.h"
#include <limits.h>

size_t	ft_numlen(int n)
{
	size_t	len;

	len = 1;
	if (n == INT_MIN)
	{
		len += 2;
		n /= 10;
		n = -n;
	}
	else if (n < 0)
	{
		len++;
		n = -n;
	}
	while (n >= 10)
	{
		len++;
		n /= 10;
	}
	return (len);
}
